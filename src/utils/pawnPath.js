export function getPawnCoordinates(player, pos, index) {
  const size = 35
  if (pos === -1) {
    // casa inicial
    const homeMap = {
      red: [[40, 40], [80, 40], [40, 80], [80, 80]],
      green: [[360, 40], [400, 40], [360, 80], [400, 80]],
      yellow: [[40, 360], [80, 360], [40, 400], [80, 400]],
      blue: [[360, 360], [400, 360], [360, 400], [400, 400]],
    }
    return homeMap[player][index]
  }

  // posições ao longo do caminho
  const path = generatePathFor(player)
  if (pos >= 0 && pos < path.length) {
    return path[pos]
  }

  return [0, 0]
}

